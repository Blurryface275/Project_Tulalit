    private void loadData(ArrayList<Keranjang> data) {
        listKeranjang.clear();
        listKeranjang.addAll(data);
    }

    private ArrayList<Keranjang> parseStringToList(String data) {
        ArrayList<Keranjang> result = new ArrayList<>();
        
        String[] keranjangStrings = data.split("#"); // pisah antar keranjang
        for(String keranjangStr : keranjangStrings) {
            if (!keranjangStr.trim().isEmpty()) {
                String[] fields = keranjangStr.split("\\|");
                
                String username = fields[0];
                String creatorName = fields[1];
                String eventName = fields[2];
                String jenis = fields[3];
                double price = Double.parseDouble(fields[4]);
                int quantity = Integer.parseInt(fields[5]);
                double total = Double.parseDouble(fields[6]);
                
                Keranjang k = new Keranjang(username, creatorName, eventName, jenis, price, quantity, total);

                if (FormLogin.user.equals(username)) {
                    result.add(k);
                }
            }
        }
        return result;
    }

    private void tampilkanKeTabel() {
        DefaultTableModel model = (DefaultTableModel) tabelCekout.getModel();
        model.setRowCount(0); // clear existing rows

        for (Keranjang k : listKeranjang) {

            Object[] row = {
                k.getCreatorName(),
                k.getEventName(),
                k.getJenis(),
                k.getPrice(),
                k.getQuantity(),
                k.getTotal()
                // setelah stock ini ada kolom untuk aksi, yang nantinya terisi dengan button untuk delete tiket yang batal dibeli
            };
            model.addRow(row);
        }
    }
    
    private void tampilkanTotalCheckout() {
        double total = 0;
        for (Keranjang k : listKeranjang) {
            double subtotal = k.getTotal();
            total += subtotal;
        }
        labelTotal.setText(Double.toString(total));
    }

