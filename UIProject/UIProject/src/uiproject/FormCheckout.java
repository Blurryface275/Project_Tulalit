                ArrayList<Keranjang> parsedList = parseStringToList(receivedData);
                loadData(parsedList);
                tampilkanKeTabel();
                tampilkanTotalCheckout();
            
                this.setLocationRelativeTo(null);
                this.setVisible(true);
                
